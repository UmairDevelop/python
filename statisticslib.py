import statistics

data = [1, 2, 3, 4, 5]
print(statistics.mean(data)) # Calculates the mean (average) of the data
print(statistics.median(data)) # Calculates the median (middle value) of the data
print(statistics.mode(data)) # Calculates the mode (most common value) of the data
print(statistics.stdev(data)) # Calculates the standard deviation of the data

print(statistics.variance(data)) # Calculates the variance of the data
print(statistics.pvariance(data)) # Calculates the population variance of the data
print(statistics.pstdev(data)) # Calculates the population standard deviation of the data